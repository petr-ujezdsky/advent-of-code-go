package main

import (
	"github.com/petr-ujezdsky/advent-of-code-go/utils"
	"github.com/stretchr/testify/assert"
	"os"
	"testing"
)

func Test_01_parse(t *testing.T) {
	reader, err := os.Open("data-00-example.txt")
	assert.Nil(t, err)

	world := ParseInput(reader)

	assert.Equal(t, 16, len(world.Items))
	assert.Equal(t, Item{Value: "467", Number: 467}, world.Items[utils.Vector2i{}])
	assert.Equal(t, Item{Value: "755", Number: 755}, world.Items[utils.Vector2i{X: 6, Y: 7}])
	assert.Equal(t, Item{Value: "$", Number: -1}, world.Items[utils.Vector2i{X: 3, Y: 8}])
}

