package main

import (
	"github.com/stretchr/testify/assert"
	"os"
	"testing"
)

func Test_01_parse(t *testing.T) {
	reader, err := os.Open("data-00-example.txt")
	assert.Nil(t, err)

	world := ParseInput(reader)

	assert.Equal(t, 2, len(world.Readings))

	reading := world.Readings[1]
	assert.Equal(t, 9, len(reading.Columns))
	assert.Equal(t, uint64(109), reading.Columns[0])

	assert.Equal(t, 7, len(reading.Rows))
	assert.Equal(t, uint64(281), reading.Rows[0])
}

