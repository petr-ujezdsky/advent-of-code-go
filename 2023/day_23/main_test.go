func Test_01_parse(t *testing.T) {
	reader, err := os.Open("data-00-example.txt")
	assert.Nil(t, err)

	world := ParseInput(reader)

	assert.Equal(t, utils.Vector2i{X: 1}, world.Start)
	assert.Equal(t, utils.Vector2i{X: 21, Y: 22}, world.End)
}

